package org.eclipse.dataspaceconnector.extensions.listener;

class ReturnObject {
    public String status;
    public String hash;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }
}
